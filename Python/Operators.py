#Arithmetic Operators
a = 17
b = 4
print("Addition:", a + b)          # Output: 21
print("Subtraction:", a - b)       # Output: 13
print("Multiplication:", a * b)    # Output: 68
print("Division:", a / b)          # Output: 4.25
print("Modulus:", a % b)           # Output: 1
print("Floor Division:", a // b)   # Output: 1

#Logical Operators
x = True
y = False
print("AND:", x and y)             # Output: False
print("OR:", x or y)               # Output: True   
print("NOT x:", not x)             # Output: False
print("NOT y:", not y)             # Output: True

#Comparison Operators
num1 = 10
num2 = 20
print("Equal:", num1 == num2)      # Output: False 
print("Not Equal:", num1 != num2)  # Output: True
print("Greater Than:", num1 > num2) # Output: False
print("Less Than:", num1 < num2)   # Output: True
print("Greater or Equal:", num1 >= num2) # Output: False
print("Less or Equal:", num1 <= num2)    # Output: True

#Assignment Operators
value = 5
print("Initial Value:", value)      # Output: 5

value += 3
print("After += 3:", value)         # Output: 8

value *= 2
print("After *= 2:", value)         # Output: 16

value -= 4
print("After -= 4:", value)         # Output: 12

value /= 3
print("After /= 3:", value)         # Output: 4.0
