# String slicing is the cutting of parts of strings
# Syntax: string[start:end:step]

value  = "Plasmodium"

#getting the first 4 characters
print(value[:4])  #Output: Data

#getting last 4 characters
print(value[-4:])

#getting characters from index 2 to index 6
print(value[2:7])  #Output: asmod

print(value[-5:-2]) #Output: diu